export const getPostById = (id: string): Post | undefined => {
  return mockPosts.find(p => p.id === id);
};

export const getCommentsByPostId = (postId: string): Comment[] => {
  return mockComments.filter(c => c.postId === postId);
};

export const getPostsByCategory = (category: string): Post[] => {
  if (category === 'all') return mockPosts;
  return mockPosts.filter(p => p.category === category);
};

export const getPostsByBuilding = (building: string): Post[] => {
  return mockPosts.filter(p => p.building === building);
};

export const getHotPosts = (): Post[] => {
  return mockPosts.filter(p => p.isHot);
};
